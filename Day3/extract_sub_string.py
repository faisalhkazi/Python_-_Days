my_text = "The is the manchester united football club. This the is the club with great history"
print(my_text[5:18])        #This will give the result from index 5 to 18

print(my_text[2:])          #This will give the result from index 2 till end

print(my_text[2:10:3])      #This last number will skip the index from the range 2 till 10